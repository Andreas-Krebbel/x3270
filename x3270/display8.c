/*
 * Initialize or re-initialize the display character set.
 * An index into the d8 tabe, or 0 for failure.
 */
int
display8_init(char *cset)
{
    	int i;

	for (i = 0; d8[i].cset; i++) {
	    	if (!strcasecmp(cset, d8[i].cset)) {
			return i;
		}
	}
	return -1;
}

/*
 * Map a Unicode character onto the display character set.
 * Returns 0 if there is no mapping.
 */
int
display8_lookup(int d8_ix, unsigned long ucs4)
{
    	int i;

    	if (d8_ix < 0)
		return 0;

	for (i = 0; i < 256; i++) {
	    	if (d8[d8_ix].u[i] == ucs4)
		    	return i;
	}
	return 0;
}
