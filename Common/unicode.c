/*
 * Translate a single EBCDIC character to Unicode for display purposes.
 *
 * EBCDIC 'FM' and 'DUP' characters are returned as '*' and ';' respectively;
 *  real 3270 terminals display these with an overscore.
 * If blank_undef is set, other undisplayable characters are returned as
 *  spaces; otherwise they are returned as 0.
 */
unsigned long
ebcdic_to_unicode(unsigned short c, Boolean blank_undef, Boolean for_display)
{
    if (c == 0x40)
	return 0x0020;

    if (c >= UT_OFFSET && c < 0xff) {
	unsigned short uc = cur_uni->code[c - UT_OFFSET];

	return uc? uc: (blank_undef? ' ': 0);

    } else switch (c) {

	case EBC_dup:
	    return for_display? 0xf8ff: '*';
	case EBC_fm:
	    return for_display? 0xf8fe: ';';
	case EBC_eo:
	    return 0x25cf; /* solid circle */
	default:
	    if (blank_undef)
		return ' ';
	    else
		return 0;
    }
}

/*
 * Map a UCS-4 character to an EBCDIC character.
 * Returns 0 for failure, nonzero for success.
 */
unsigned short
unicode_to_ebcdic(unsigned long u)
{
    int i;

    if (!u)
	return 0;
    if (u == 0x0020)
	return 0x40;

    for (i = 0; i < UT_SIZE; i++) {
	if (cur_uni->code[i] == u) {
	    return UT_OFFSET + i;
	}
    }

    return 0;
}

/*
 * Set the EBCDIC-to-Unicode translation table.
 * Returns 0 for success, -1 for failure.
 */
int
set_uni(const char *csname, const char **codepage,
	const char **display_charsets)
{
	int i;
	const char *realname;
	int rc = -1;

	realname = csname;

	/* Search for an alias. */
	for (i = 0; cpaliases[i].alias != NULL; i++) {
		if (!strcasecmp(csname, cpaliases[i].alias)) {
			realname = cpaliases[i].canon;
			break;
		}
	}

	/* Search for a match. */
	for (i = 0; uni[i].name != NULL; i++) {
		if (!strcasecmp(realname, uni[i].name)) {
			cur_uni = &uni[i];
			*codepage = uni[i].codepage;
			*display_charsets = uni[i].display_charset;
			rc = 0;
			break;
		}
	}

#if defined(USE_ICONV) /*[*/
	/*
	 * wchar_t's are not Unicode, so getting to/from Unicode is only half
	 * the battle.  We need to use iconv() to get between Unicode to the
	 * local multi-byte representation.  We'll explicitly use UTF-8, which
	 * appears to be the most broadly-supported translation.
	 */
	if (rc == 0) {
		if (!strcasecmp(locale_codeset, "UTF8") ||
			!strcasecmp(locale_codeset, "UTF-8")) {
			is_utf8 = True;
		} else {
			/*
			 * If iconv doesn't support the locale's codeset, then
			 * this box is hosed.
			 */
			i_u2mb = iconv_open(locale_codeset, "UTF-8");
			if (i_u2mb == (iconv_t)(-1))
				rc = -1;
			else {
				i_mb2u = iconv_open("UTF-8", locale_codeset);
				if (i_mb2u == (iconv_t)(-1)) {
					iconv_close(i_u2mb);
					rc = -1;
				}
			}
		}
	}
#endif /*]*/

	return rc;
}

